    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTabelaCategorias.getSelectedRow();
        
        if (linhaSelecionada >= 0) {
            int codigoCategoria = (int) jTabelaCategorias.getValueAt(linhaSelecionada, 0);
            String nomeCategoria = (String) jTabelaCategorias.getValueAt(linhaSelecionada, 1);
            
            TelaCadastroCategoria telaCadCategoria = new TelaCadastroCategoria(true, new Categoria(codigoCategoria, nomeCategoria));
            TelaPrincipal.jDesktopPane1.add(telaCadCategoria);
            telaCadCategoria.setVisible(true);
            jButton1ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


