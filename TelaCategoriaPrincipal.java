    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTabelaCategorias.getSelectedRow();
        
        if (linhaSelecionada >= 0) {
            int codigoCategoria = (int) jTabelaCategorias.getValueAt(linhaSelecionada, 0);
            CategoriaDAO categoriaBanco = new CategoriaDAO();
            categoriaBanco.deletarCategoria(codigoCategoria);
            jButton1ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

